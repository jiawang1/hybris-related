package com.tasly.anguo.storefront.forms;


public class PersonalIdentifyForm {

	private String idName;
	private String idCard;
	private String bank;
	private String accountOwer;
	private String accountNumber;

	/**
	 * @return the idName
	 */
	public String getIdName() {
		return idName;
	}

	/**
	 * @param idName
	 *            the idName to set
	 */
	public void setIdName(String idName) {
		this.idName = idName;
	}

	/**
	 * @return the idCard
	 */
	public String getIdCard() {
		return idCard;
	}

	/**
	 * @param idCard
	 *            the idCard to set
	 */
	public void setIdCard(String idCard) {
		this.idCard = idCard;
	}

	/**
	 * @return the bank
	 */
	public String getBank() {
		return bank;
	}

	/**
	 * @param bank
	 *            the bank to set
	 */
	public void setBank(String bank) {
		this.bank = bank;
	}

	/**
	 * @return the accountOwer
	 */
	public String getAccountOwer() {
		return accountOwer;
	}

	/**
	 * @param accountOwer
	 *            the accountOwer to set
	 */
	public void setAccountOwer(String accountOwer) {
		this.accountOwer = accountOwer;
	}

	/**
	 * @return the accountNumber
	 */
	public String getAccountNumber() {
		return accountNumber;
	}

	/**
	 * @param accountNumber
	 *            the accountNumber to set
	 */
	public void setAccountNumber(String accountNumber) {
		this.accountNumber = accountNumber;
	}

}
